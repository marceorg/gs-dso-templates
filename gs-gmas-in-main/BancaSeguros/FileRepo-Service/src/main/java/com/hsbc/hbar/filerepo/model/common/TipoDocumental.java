package com.hsbc.hbar.filerepo.model.common;

public class TipoDocumental {
	private Integer id;
	private String name;

	public TipoDocumental(final Integer id, final String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public Integer getId() {
		return this.id;
	}

	public void setId(final Integer id) {
		this.id = id;
	}

	public String getName() {
		return this.name;
	}

	public void setName(final String name) {
		this.name = name;
	}
}
